import PyTango
from Lima import Core
from Lima import Princeton as PrincetonAcq
from Lima.Server import AttrHelper


class Princeton(PyTango.Device_4Impl):

    Core.DEB_CLASS(Core.DebModApplication, 'LimaCCDs')


#------------------------------------------------------------------
#    Device constructor
#------------------------------------------------------------------
    def __init__(self,*args) :
        PyTango.Device_4Impl.__init__(self,*args)

        self.__SensorTemperatureStatus = {"FAULT" : PrincetonAcq.Interface.Temp_Fault,
                                          "LOCKED": PrincetonAcq.Interface.Temp_Locked,
                                          "UNLOCKED": PrincetonAcq.Interface.Temp_Unlocked,
                                          }
        self.__Attribute2FunctionBase = {
        }
        
        self.init_device()

#------------------------------------------------------------------
#    Device destructor
#------------------------------------------------------------------
    def delete_device(self):
        pass

#------------------------------------------------------------------
#    Device initialization
#------------------------------------------------------------------
    @Core.DEB_MEMBER_FUNCT
    def init_device(self):
        self.set_state(PyTango.DevState.ON)
        self.get_device_properties(self.get_device_class())

#------------------------------------------------------------------
#    getAttrStringValueList command:
#
#    Description: return a list of authorized values if any
#    argout: DevVarStringArray
#------------------------------------------------------------------
    @Core.DEB_MEMBER_FUNCT
    def getAttrStringValueList(self, attr_name):
        #use AttrHelper
        return AttrHelper.get_attr_string_value_list(self, attr_name)
#==================================================================
#
#    Princeton read/write attribute methods
#
#==================================================================
    def __getattr__(self,name) :
        #use AttrHelper
        return AttrHelper.get_attr_4u(self,name,_PrincetonInterface)


#==================================================================
#
#    PrincetonClass class definition
#
#==================================================================
class PrincetonClass(PyTango.DeviceClass):

    class_property_list = {}

    device_property_list = {
        'camera_serial':
        [PyTango.DevString,
         "Camera Serial", ""],
        }

    cmd_list = {
        'getAttrStringValueList':
        [[PyTango.DevString, "Attribute name"],
         [PyTango.DevVarStringArray, "Authorized String value list"]],
        }

    attr_list = {
        'sensor_temperature_status':
        [[PyTango.DevString,
          PyTango.SCALAR,
          PyTango.READ],
         {
             'unit': 'N/A',
             'format': '',
             'description': 'get cooling temperature status',
         }],
        'sensor_temperature':
        [[PyTango.DevDouble,
          PyTango.SCALAR,
          PyTango.READ],
         {
             'unit': 'N/A',
             'format': '',
             'description': 'current ccd temperature',
         }],
        'sensor_temperature_setpoint':
        [[PyTango.DevDouble,
          PyTango.SCALAR,
          PyTango.READ_WRITE],
         {
             'unit': 'N/A',
             'format': '',
             'description': 'regulation temperature setpoint',
         }],
    }

    def __init__(self,name) :
        PyTango.DeviceClass.__init__(self,name)
        self.set_type(name)

#----------------------------------------------------------------------------
# Plugins
#----------------------------------------------------------------------------
_PrincetonInterface = None

def get_control(camera_serial) :
    global _PrincetonInterface
    if _PrincetonInterface is None:
        _PrincetonInterface = PrincetonAcq.Interface(camera_serial)
    return Core.CtControl(_PrincetonInterface)

def get_tango_specific_class_n_device():
    return PrincetonClass,Princeton
